    private void imageLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imageLabelMouseClicked
        //color picker on the image
        int x = (int) ((evt.getX()-this.dx) * this.r);
        int y = (int) ((evt.getY()-this.dy) * this.r);
        if(x>0 && y>0 && x<this.img.getWidth() && y<this.img.getHeight()){
            int rgb = this.img.getRGB(x, y);
            int red = (rgb & 0x00ff0000) >> 16;
            int green = (rgb & 0x0000ff00) >> 8;
            int blue = rgb & 0x000000ff;
            this.jTextField1.setText(String.format("#%02x%02x%02x", red, green, blue));
            this.jPanel1.setBackground(new Color(red,green,blue));
        }
    }//GEN-LAST:event_imageLabelMouseClicked

