    private void setPreview(BufferedImage b){
        
        r = Math.max(b.getWidth()/this.IMG_WIDTH, b.getHeight()/this.IMG_HEIGHT);
        int newWidth = (int) (b.getWidth() / r);//new dimension of the image
        int newHeight = (int) (b.getHeight() / r);
        this.dx = (int) (this.IMG_WIDTH - newWidth)/2;//margin in the jLabel
        this.dy = (int) (this.IMG_HEIGHT - newHeight)/2;
        
        BufferedImage resized = new BufferedImage(newWidth, newHeight, b.getType());
        Graphics2D g = resized.createGraphics();
        g.setRenderingHint(RenderingHints.KEY_INTERPOLATION,RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g.drawImage(b, 0, 0, newWidth, newHeight, 0, 0, b.getWidth(),b.getHeight(), null);
        g.dispose();
        ImageIcon imageIcon = new ImageIcon(resized);
        this.imageLabel.setIcon(imageIcon);
    }
    
    
